import {Suspense, useEffect, useState} from 'react';
import {Await, NavLink, useAsyncValue} from 'react-router';
import {useAnalytics, useOptimisticCart} from '@shopify/hydrogen';
import {useAside} from '~/components/Aside';

/**
 * @param {HeaderProps}
 */
export function Header({header, isLoggedIn, cart, publicStoreDomain}) {
  const {shop, menu} = header;
  const [isOverlay, setIsOverlay] = useState(false);

  useEffect(() => {
    if (typeof document === 'undefined') return;
    const hero = document.getElementById('home-hero');
    if (!hero) {
      setIsOverlay(false);
      return;
    }
    const observer = new IntersectionObserver(
      ([entry]) => setIsOverlay(entry.isIntersecting),
      {threshold: 0.2},
    );
    observer.observe(hero);
    return () => observer.disconnect();
  }, []);

  const baseClasses =
    'fixed inset-x-0 top-0 z-30 flex items-center justify-between px-6 py-4 lg:px-10 transition-colors duration-200';
  const overlayClasses = 'bg-transparent text-brand-bg shadow-none border-none';
  const solidClasses =
    'bg-brand-bg/95 text-brand-text shadow-subtle border-b border-brand-accent/10';

  return (
    <header
      className={`${baseClasses} ${isOverlay ? overlayClasses : solidClasses}`}
    >
      <NavLink
        prefetch="intent"
        to="/"
        style={activeLinkStyle}
        end
        className={`flex items-center gap-3 ${
          isOverlay ? 'text-brand-bg' : 'text-brand-text'
        }`}
      >
        <img
          src="/logo.svg"
          alt={shop.name}
          className={`h-10 w-auto drop-shadow ${
            isOverlay ? 'brightness-0 invert' : ''
          }`}
        />
        <span className="sr-only">{shop.name}</span>
      </NavLink>
      <HeaderMenu
        menu={menu}
        viewport="desktop"
        primaryDomainUrl={header.shop.primaryDomain.url}
        publicStoreDomain={publicStoreDomain}
        isOverlay={isOverlay}
      />
      <HeaderCtas isLoggedIn={isLoggedIn} cart={cart} isOverlay={isOverlay} />
    </header>
  );
}

/**
 * @param {{
 *   menu: HeaderProps['header']['menu'];
 *   primaryDomainUrl: HeaderProps['header']['shop']['primaryDomain']['url'];
 *   viewport: Viewport;
 *   publicStoreDomain: HeaderProps['publicStoreDomain'];
 * }}
 */
export function HeaderMenu({
  menu,
  primaryDomainUrl,
  viewport,
  publicStoreDomain,
  isOverlay = false,
}) {
  const className = `header-menu-${viewport} ${
    viewport === 'desktop'
      ? `hidden lg:flex items-center gap-8 ${
          isOverlay ? 'text-brand-bg' : 'text-brand-text'
        }`
      : 'flex flex-col gap-6 text-brand-text'
  }`;
  const {close} = useAside();

  return (
    <nav className={className} role="navigation">
      {viewport === 'mobile' && (
        <NavLink
          end
          onClick={close}
          prefetch="intent"
          style={activeLinkStyle}
          to="/"
        >
          Home
        </NavLink>
      )}
      {(menu || FALLBACK_HEADER_MENU).items.map((item) => {
        if (!item.url) return null;

        // if the url is internal, we strip the domain
        const url =
          item.url.includes('myshopify.com') ||
          item.url.includes(publicStoreDomain) ||
          item.url.includes(primaryDomainUrl)
            ? new URL(item.url).pathname
            : item.url;
        return (
          <NavLink
            className="header-menu-item"
            end
            key={item.id}
            onClick={close}
            prefetch="intent"
            style={activeLinkStyle}
            to={url}
          >
            {item.title}
          </NavLink>
        );
      })}
    </nav>
  );
}

/**
 * @param {Pick<HeaderProps, 'isLoggedIn' | 'cart'>}
 */
function HeaderCtas({isLoggedIn, cart, isOverlay}) {
  return (
    <nav
      className={`header-ctas flex items-center gap-4 ${
        isOverlay ? 'text-brand-bg' : 'text-brand-text'
      }`}
      role="navigation"
    >
      <HeaderMenuMobileToggle />
      <NavLink prefetch="intent" to="/account" style={activeLinkStyle}>
        <Suspense fallback="Sign in">
          <Await resolve={isLoggedIn} errorElement="Sign in">
            {(isLoggedIn) => (isLoggedIn ? 'Account' : 'Sign in')}
          </Await>
        </Suspense>
      </NavLink>
      <SearchToggle />
      <CartToggle cart={cart} />
    </nav>
  );
}

function HeaderMenuMobileToggle() {
  const {open} = useAside();
  return (
    <button
      className="header-menu-mobile-toggle reset text-current"
      onClick={() => open('mobile')}
    >
      <h3>â˜°</h3>
    </button>
  );
}

function SearchToggle() {
  const {open} = useAside();
  return (
    <button className="reset" onClick={() => open('search')}>
      Search
    </button>
  );
}

/**
 * @param {{count: number | null}}
 */
function CartBadge({count}) {
  const {open} = useAside();
  const {publish, shop, cart, prevCart} = useAnalytics();

  return (
    <a
      href="/cart"
      onClick={(e) => {
        e.preventDefault();
        open('cart');
        publish('cart_viewed', {
          cart,
          prevCart,
          shop,
          url: window.location.href || '',
        });
      }}
    >
      Cart {count === null ? <span>&nbsp;</span> : count}
    </a>
  );
}

/**
 * @param {Pick<HeaderProps, 'cart'>}
 */
function CartToggle({cart}) {
  return (
    <Suspense fallback={<CartBadge count={null} />}>
      <Await resolve={cart}>
        <CartBanner />
      </Await>
    </Suspense>
  );
}

function CartBanner() {
  const originalCart = useAsyncValue();
  const cart = useOptimisticCart(originalCart);
  return <CartBadge count={cart?.totalQuantity ?? 0} />;
}

